'use strict';

/**
 * @module ui/status.view
 * Рендер строки фазы (лобби/итог) и нижней карточки таймера: подпись «кто
 * думает» + таймер + кнопка паузы. Чей сейчас ход в активной игре показывает не
 * текстовая «плашка», а неоновая подсветка карточки команды (task 4) — поэтому
 * верхняя строка фазы в активных фазах скрыта.
 */

import { $ } from '../util/dom.js';
import { getState } from '../state/store.js';

/**
 * Перерисовывает строку фазы и нижнюю карточку таймера. Верхняя строка видна
 * только в лобби/после игры (в активных фазах чей ход — по неоновой подсветке
 * карточки команды, task 4). Нижняя карточка видна только в игре: подпись
 * «Лидер думает:» (фаза clue — ход капитана) либо «Команда думает:» (фаза guess —
 * команда угадывает), таймер и кнопка паузы. На паузе подпись и значение таймера
 * не меняются (показывают обычный текст и оставшееся время), переключается только
 * подпись кнопки «⏸ Пауза» ↔ «▶️ Возобновить» — стиль кнопки (btn-ghost) единый.
 * Вызывается на каждый тик — таймер обновляется ежесекундно.
 * @returns {void}
 */
export function renderStatus() {
  const state = getState();
  const inGame = state.phase === 'clue' || state.phase === 'guess';

  // Верхняя строка фазы — только в лобби/после игры.
  const el = $('#status-text');
  let txt = '';
  if (state.phase === 'lobby') txt = '🛋 Лобби — распределите команды и начните игру';
  else if (state.phase === 'over') txt = state.winner ? `🏆 Победили ${state.winner === 'red' ? 'красные' : 'синие'}!` : 'Игра окончена';
  el.textContent = txt;
  $('#status-bar').classList.toggle('hidden', txt === '');

  // Нижняя карточка таймера — только в активной игре.
  $('#timer-bar').classList.toggle('hidden', !inGame);
  if (inGame) {
    // clue — думает капитан (даёт подсказку), guess — угадывает команда.
    // На паузе подпись и таймер остаются обычными (без «⏸ Пауза» слева и иконки
    // паузы справа) — текущее оставшееся время видно и во время паузы.
    $('#turn-label').textContent = state.phase === 'clue' ? 'Лидер думает:' : 'Команда думает:';
    $('#timer-val').textContent = state.timer;
    $('#timer').classList.toggle('low', state.timer <= 10);
    // Одна кнопка-переключатель: меняется только подпись, стиль (btn-ghost)
    // одинаков для «Пауза» и «Возобновить».
    $('#pause-btn').textContent = state.paused ? '▶️ Возобновить' : '⏸ Пауза';
  }
}
